def generate_file_content(lang_code, translations):
    """Generate the Python file content for a language."""
    lang_names = {
        "ar": "Arabic", "cs": "Czech", "da": "Danish", "de": "German",
        "el": "Greek", "es": "Spanish", "fi": "Finnish", "fr": "French",
        "hi": "Hindi", "hu": "Hungarian", "in": "Indonesian", "is": "Icelandic",
        "it": "Italian", "iw": "Hebrew", "ja": "Japanese", "ko": "Korean",
        "ms": "Malay", "nb": "Norwegian Bokmal", "nl": "Dutch", "pl": "Polish",
        "pt": "Portuguese", "ro": "Romanian", "ru": "Russian", "sv": "Swedish",
        "th": "Thai", "tr": "Turkish", "uk": "Ukrainian", "ur": "Urdu",
        "vi": "Vietnamese", "zh": "Chinese"
    }

    lang_name = lang_names.get(lang_code, lang_code.upper())

    lines = [
        f"# {lang_name} translations for NeuroComet",
        f"# values-{lang_code}",
        "",
        "TRANSLATIONS = {"
    ]

    for key, value in translations.items():
        # Escape quotes in value
        escaped_value = value.replace('\\', '\\\\').replace('"', '\\"')
        lines.append(f'    "{key}": "{escaped_value}",')

    lines.append("}")
    lines.append("")

    return "\n".join(lines)


